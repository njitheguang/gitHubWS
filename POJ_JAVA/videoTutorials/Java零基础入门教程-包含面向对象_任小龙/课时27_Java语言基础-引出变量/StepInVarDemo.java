package com.train.day02;

public class StepInVarDemo {
	// * 程序入口函数
	// * 测试
	public static void main(String[] args) {
		System.out.println(6 + 3);
		System.out.println(6 - 3);
		System.out.println(6 * 3);
		System.out.println(6 / 3);
		System.out.println(8 + 4);
		System.out.println(8 - 4);
		System.out.println(8 * 4);
		System.out.println(8 / 4);
		int x = 6;
		int y = 3;
		System.out.println(x + y);
		System.out.println(x - y);
		System.out.println(x * y);
		System.out.println(x / y);
	}
}
